            val lines = current.lines().filter { it.isNotEmpty() }.toMutableList()
            var hasUrl = false
            var hasModel = false
            var hasInstallLlamaServer = false
            val migrated = lines.map { line ->
                val trimmed = line.trimStart()
                val assignment = if (trimmed.startsWith("export ")) trimmed.removePrefix("export ").trimStart() else trimmed
                when {
                    assignment.startsWith("LOCAL_LLM_URL=") -> {
                        hasUrl = true
                        line
                    }
                    assignment == "LOCAL_LLM_MODEL=Qwen3-8B" -> {
                        hasModel = true
                        line.replace("LOCAL_LLM_MODEL=Qwen3-8B", "LOCAL_LLM_MODEL=Qwen3.5-4B-Q4_K_M")
                    }
                    assignment == "LOCAL_LLM_MODEL=\"Qwen3-8B\"" || assignment == "LOCAL_LLM_MODEL='Qwen3-8B'" -> {
                        hasModel = true
                        line.replace("LOCAL_LLM_MODEL=\"Qwen3-8B\"", "LOCAL_LLM_MODEL=Qwen3.5-4B-Q4_K_M")
                            .replace("LOCAL_LLM_MODEL='Qwen3-8B'", "LOCAL_LLM_MODEL=Qwen3.5-4B-Q4_K_M")
                    }
                    assignment == "LOCAL_LLM_MODEL=Qwen3-8B-Q4_K_M" ||
                        assignment == "LOCAL_LLM_MODEL=Qwen3-4B-Instruct-2507-Q4_K_M" ||
                        assignment == "LOCAL_LLM_MODEL=\"Qwen3-8B-Q4_K_M\"" ||
                        assignment == "LOCAL_LLM_MODEL='Qwen3-8B-Q4_K_M'" ||
                        assignment == "LOCAL_LLM_MODEL=\"Qwen3-4B-Instruct-2507-Q4_K_M\"" ||
                        assignment == "LOCAL_LLM_MODEL='Qwen3-4B-Instruct-2507-Q4_K_M'" -> {
                        hasModel = true
                        line.replace(Regex("LOCAL_LLM_MODEL=(['\"]?)Qwen3-(8B-Q4_K_M|4B-Instruct-2507-Q4_K_M)\\1"), "LOCAL_LLM_MODEL=Qwen3.5-4B-Q4_K_M")
                    }
                    assignment.startsWith("LOCAL_LLM_MODEL=") -> {
                        hasModel = true
                        line
                    }
                    assignment.startsWith("LOCAL_LLM_INSTALL_LLAMA_SERVER=") -> {
                        hasInstallLlamaServer = true
                        line
                    }
                    else -> line
                }
            }.toMutableList()
            if (!hasUrl) migrated.add("LOCAL_LLM_URL=http://127.0.0.1:8080")
            if (!hasModel) migrated.add("LOCAL_LLM_MODEL=Qwen3.5-4B-Q4_K_M")
            if (!hasInstallLlamaServer) migrated.add("LOCAL_LLM_INSTALL_LLAMA_SERVER=1")
            envFile.writeText(migrated.joinToString("\n") + "\n")
        } catch (e: Exception) {
            android.util.Log.w("HomeInitializer", "agent env migration skipped: ${e.message}")
        }
    }

    private fun migrateExistingAgentEnv(home: File) {
        val envFile = File(home, ".shelly/agents/.env")
        if (envFile.exists()) {
            migrateContentAgentEnv(envFile)
        }
    }

    private val CONTENT_AGENT_VERSION = 4

    private fun seedAgent(
        agentsDir: File,
        id: String,
        name: String,
        description: String,
        prompt: String,
        toolJson: String,
        outputPath: String,
        schedule: String? = null,
        inheritedState: JSONObject? = null,
    ) {
        val file = File(agentsDir, "$id.json")
        val nextContent = agentJson(id, name, description, prompt, toolJson, outputPath, schedule)
        if (!file.exists()) {
            file.parentFile?.mkdirs()
            val seeded = JSONObject(nextContent)
            applyAgentState(seeded, inheritedState)
            file.writeText(seeded.toString(2) + "\n")
            return
        }

        try {
            val current = JSONObject(file.readText())
            if (current.optInt("version", 1) >= CONTENT_AGENT_VERSION) return

            val migrated = JSONObject(nextContent)
            applyAgentState(migrated, current)
            migrated.put("version", CONTENT_AGENT_VERSION)
            file.writeText(migrated.toString(2) + "\n")
        } catch (e: Exception) {
            android.util.Log.w("HomeInitializer", "agent seed migration skipped for $id: ${e.message}")
        }
    }

    private fun applyAgentState(target: JSONObject, state: JSONObject?) {
        if (state == null) return
        target.put("enabled", state.optBoolean("enabled", target.optBoolean("enabled", true)))
        if (state.has("lastRun") && !state.isNull("lastRun")) {
            target.put("lastRun", state.get("lastRun"))
        }
        if (state.has("lastResult") && !state.isNull("lastResult")) {
            target.put("lastResult", state.get("lastResult"))
        }
        target.put("createdAt", state.optLong("createdAt", target.optLong("createdAt", 0L)))
    }

    private fun agentJson(
        id: String,
        name: String,
        description: String,
        prompt: String,
        toolJson: String,
        outputPath: String,
        schedule: String?,
    ): String =
        """
{
  "id": ${jsonString(id)},
  "name": ${jsonString(name)},
  "description": ${jsonString(description)},
  "prompt": ${jsonString(prompt)},
  "schedule": ${schedule?.let { jsonString(it) } ?: "null"},
  "tool": $toolJson,
  "outputPath": ${jsonString(outputPath)},
  "outputTemplate": null,
  "enabled": true,
  "lastRun": null,
  "lastResult": null,
  "createdAt": 0,
  "version": $CONTENT_AGENT_VERSION
}
""".trimIndent() + "\n"

    private fun jsonString(value: String): String =
        "\"" + value
            .replace("\\", "\\\\")
            .replace("\"", "\\\"")
            .replace("\n", "\\n") + "\""

    private fun writeIfMissing(file: File, content: String) {
        if (file.exists()) return
        file.parentFile?.mkdirs()
        file.writeText(content)
    }
