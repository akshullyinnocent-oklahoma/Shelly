            sb.appendLine("  if [ \$__healthy -eq 1 ]; then")
            sb.appendLine("    local __new_codex=\"\$__staging/node_modules/@openai/codex/bin/codex.js\"")
            sb.appendLine("    if [ -f \"\$__new_codex\" ]; then")
            sb.appendLine("      if ! timeout 30 /system/bin/linker64 \"$libDir/node\" \"\$__new_codex\" --version 2>&1 | grep -Eq \"\$__ver_re\"; then")
            sb.appendLine("        echo '[health] codex.js --version FAILED' >&2")
            sb.appendLine("        __healthy=0")
            sb.appendLine("      else")
            sb.appendLine("        echo '[health] codex.js --version OK'")
            sb.appendLine("      fi")
            sb.appendLine("    else")
            sb.appendLine("      echo '[health] codex.js missing' >&2")
            sb.appendLine("      __healthy=0")
            sb.appendLine("    fi")
            sb.appendLine("  fi")
            // 5. Promote or discard staging. On success we rotate the old
            //    live tree into .shelly-cli.prev (last-known-good snapshot)
            //    and promote staging to the live path. On failure we delete
            //    staging without touching the live tree, and skip the
            //    update marker so the next shell launch retries.
            sb.appendLine("  if [ \$__healthy -eq 1 ]; then")
            sb.appendLine("    echo '[install] health check OK, promoting staging'")
            // Codex review 2026-04-25 round 2 issue #4: the previous
            // promote sequence had an intermediate broken state (mkdir
            // empty live dir between moves). Robust pattern: delete
            // .prev, atomically rename live → .prev, then atomically
            // rename staging → live. If the second rename fails, roll
            // .prev back to live. All paths quoted defensively.
            sb.appendLine("    rm -rf \"\$__prev\"")
            sb.appendLine("    local __rolled_back=0")
            sb.appendLine("    if [ -d \"\$__shelly_cli_dir\" ] && [ -d \"\$__shelly_cli_dir/node_modules\" ]; then")
            sb.appendLine("      if ! mv \"\$__shelly_cli_dir\" \"\$__prev\"; then")
            sb.appendLine("        echo '[install] FAILED to rotate live → .prev — aborting promote' >&2")
            sb.appendLine("        rm -rf \"\$__staging\"")
            sb.appendLine("        return 1")
            sb.appendLine("      fi")
            sb.appendLine("    fi")
            // Single rename of staging → live (no mkdir intermediate).
            // mv-rename is atomic on the same filesystem.
            sb.appendLine("    if ! mv \"\$__staging\" \"\$__shelly_cli_dir\"; then")
            sb.appendLine("      echo '[install] FAILED to rename staging → live; rolling back .prev' >&2")
            sb.appendLine("      if [ -d \"\$__prev\" ]; then")
            sb.appendLine("        mv \"\$__prev\" \"\$__shelly_cli_dir\" || true")
            sb.appendLine("        __rolled_back=1")
            sb.appendLine("      fi")
            sb.appendLine("      rm -rf \"\$__staging\"")
            sb.appendLine("      [ \$__rolled_back -eq 1 ] && echo '[install] rolled back to previous live tree' >&2")
            sb.appendLine("      return 1")
            sb.appendLine("    fi")
            sb.appendLine("    echo \"\$__shelly_now\" > \"\$__shelly_update_marker\"")
            sb.appendLine("    echo '[install] committed — live tree updated, .prev snapshot refreshed'")
            sb.appendLine("  else")
            sb.appendLine("    echo '[install] HEALTH CHECK FAILED — discarding staging, live tree untouched' >&2")
            // v60: record the staging versions that just failed smoke into
            // .failed-versions so the per-launch quick check below skips them
            // until upstream re-publishes (or 1h cooldown expires). Each line
            // is "<pkg>=<version> <epoch>". The quick check honours the most
            // recent record per <pkg>=<version>.
            sb.appendLine("    local __failed_log=\"\$HOME/.shelly-cli/.failed-versions\"")
            sb.appendLine("    local __now_epoch=\$(date +%s 2>/dev/null || echo 0)")
            sb.appendLine("    for __pkg in @anthropic-ai/claude-code @google/gemini-cli @openai/codex; do")
            sb.appendLine("      local __failed_pkg_json=\"\$__staging/node_modules/\$__pkg/package.json\"")
            sb.appendLine("      [ -f \"\$__failed_pkg_json\" ] || continue")
            sb.appendLine("      local __failed_v=\$(grep -oE '\"version\"[[:space:]]*:[[:space:]]*\"[^\"]+\"' \"\$__failed_pkg_json\" | head -1 | grep -oE '[0-9][0-9.]*[0-9]')")
            sb.appendLine("      [ -n \"\$__failed_v\" ] && echo \"\$__pkg=\$__failed_v \$__now_epoch\" >> \"\$__failed_log\"")
            sb.appendLine("    done")
            sb.appendLine("    rm -rf \"\$__staging\"")
            sb.appendLine("    echo '[install] will retry next shell launch (marker NOT updated)'")
            sb.appendLine("  fi")
            sb.appendLine("  echo '[install] done'")
            sb.appendLine("}")
            // v61: quick CLI version check. Runs on every shell launch in a
            // detached subshell (~3-5s, ~30KB total network). Compares
            // installed claude-code, gemini-cli, and codex npm package
            // versions against npm `latest`. If any package has a newer
            // upstream that is NOT in .failed-versions (or whose failure
            // record is older than the cooldown), kicks off the existing
            // __shelly_bg_cli_update pipeline immediately, bypassing the
            // 24h marker. The full pipeline still does staging + smoke +
            // atomic promote + rollback, so a broken upstream cannot ship.
            //
            // Bandwidth: 1× `npm view <pkg> version` per package per launch
            // when the cheap-check cooldown has expired. We additionally
            // gate the quick check itself on a short cooldown
            // (SHELLY_QUICK_CHECK_INTERVAL, default 3600s) to avoid
            // hammering npm on rapid relaunches.
            sb.appendLine("# v61: per-launch quick version check (bypasses 24h gate when newer found)")
            sb.appendLine("__shelly_quick_check_marker=\"\$HOME/.shelly-cli/.last_quick_check\"")
            sb.appendLine("__shelly_quick_check_interval=\${SHELLY_QUICK_CHECK_INTERVAL:-3600}")
            sb.appendLine("__shelly_failed_cooldown=\${SHELLY_FAILED_VERSION_COOLDOWN:-86400}")
            sb.appendLine("case \"\$__shelly_failed_cooldown\" in ''|*[!0-9]*) __shelly_failed_cooldown=86400 ;; esac")
            sb.appendLine("__shelly_quick_check_clis() {")
            sb.appendLine("  if [ -z \"\$__SHELLY_QUICK_CHECK_SUBSHELL\" ]; then")
            sb.appendLine("    ( __SHELLY_QUICK_CHECK_SUBSHELL=1 __shelly_quick_check_clis </dev/null >/dev/null 2>&1 & )")
            sb.appendLine("    return 0")
            sb.appendLine("  fi")
            sb.appendLine("  local __qlog=\"\$HOME/.shelly-cli/quick-check.log\"")
            sb.appendLine("  mkdir -p \"\$HOME/.shelly-cli\"")
            sb.appendLine("  exec </dev/null >>\"\$__qlog\" 2>&1")
            sb.appendLine("  echo \"--- \$(date) ---\"")
            sb.appendLine("  local __failed_log=\"\$HOME/.shelly-cli/.failed-versions\"")
            sb.appendLine("  local __now=\$(date +%s 2>/dev/null || echo 0)")
            sb.appendLine("  local __need_update=0")
            sb.appendLine("  local __notice=\"\"")
            // Iterate through the npm packages we expect to track @latest.
            sb.appendLine("  for __pkg in @anthropic-ai/claude-code @google/gemini-cli @openai/codex; do")
            sb.appendLine("    local __upstream=\$(timeout 15 /system/bin/linker64 \"$libDir/node\" \"$libDir/node_modules/npm/bin/npm-cli.js\" view \"\$__pkg\" version 2>/dev/null | tail -1)")
            sb.appendLine("    if [ -z \"\$__upstream\" ]; then")
            sb.appendLine("      echo \"[quick] \$__pkg: npm view failed (offline?), skipping\"")
            sb.appendLine("      continue")
            sb.appendLine("    fi")
            sb.appendLine("    local __pkg_json=\"\$HOME/.shelly-cli/node_modules/\$__pkg/package.json\"")
            sb.appendLine("    local __installed=\"\"")
            sb.appendLine("    if [ -f \"\$__pkg_json\" ]; then")
            sb.appendLine("      __installed=\$(grep -oE '\"version\"[[:space:]]*:[[:space:]]*\"[^\"]+\"' \"\$__pkg_json\" | head -1 | grep -oE '[0-9][0-9.]*[0-9]')")
            sb.appendLine("    fi")
            sb.appendLine("    if [ \"\$__upstream\" = \"\$__installed\" ]; then")
            sb.appendLine("      echo \"[quick] \$__pkg: installed \$__installed = upstream\"")
            sb.appendLine("      continue")
            sb.appendLine("    fi")
            // Honour failed-versions cooldown. Each line is "<pkg>=<ver> <epoch>";
            // a fresh failure within __shelly_failed_cooldown blocks attempts at
            // that exact upstream version.
            sb.appendLine("    local __failed_epoch=0")
            sb.appendLine("    if [ -f \"\$__failed_log\" ]; then")
            sb.appendLine("      __failed_epoch=\$(grep -E \"^\$__pkg=\$__upstream \" \"\$__failed_log\" 2>/dev/null | tail -1 | awk '{print \$2}')")
            sb.appendLine("      __failed_epoch=\${__failed_epoch:-0}")
            sb.appendLine("    fi")
            sb.appendLine("    if [ \"\$__failed_epoch\" -gt 0 ] && [ \$(( __now - __failed_epoch )) -lt \$__shelly_failed_cooldown ]; then")
            sb.appendLine("      echo \"[quick] \$__pkg: upstream \$__upstream failed smoke recently (\$(( __now - __failed_epoch ))s ago < \${__shelly_failed_cooldown}s cooldown), skipping\"")
            sb.appendLine("      continue")
            sb.appendLine("    fi")
            sb.appendLine("    echo \"[quick] \$__pkg: installed=\$__installed upstream=\$__upstream UPGRADE AVAILABLE\"")
            sb.appendLine("    __notice=\"\$__notice \$__pkg \$__installed→\$__upstream\"")
            sb.appendLine("    __need_update=1")
            sb.appendLine("  done")
            sb.appendLine("  if [ \"\$__need_update\" = \"1\" ]; then")
            sb.appendLine("    echo \"[quick] kicking off __shelly_bg_cli_update for:\$__notice\"")
            sb.appendLine("    __shelly_bg_cli_update")
            sb.appendLine("  else")
            sb.appendLine("    echo \"[quick] all up-to-date, no update fired\"")
            sb.appendLine("  fi")
            sb.appendLine("  echo \"\$__now\" > \"\$__shelly_quick_check_marker\"")
            sb.appendLine("}")
            // Quick check fires when its own cooldown has elapsed. The 24h gate
            // below remains as a fallback for the "offline at every launch"
            // pathology — even if quick check never gets to run a successful
            // npm view, the daily marker eventually triggers a full attempt.
            sb.appendLine("__shelly_last_quick_check=\$(cat \"\$__shelly_quick_check_marker\" 2>/dev/null || echo 0)")
            sb.appendLine("if [ \"\${SHELLY_AUTO_UPDATE_CLIS:-0}\" = \"1\" ] && [ \$(( __shelly_now - __shelly_last_quick_check )) -ge \$__shelly_quick_check_interval ]; then")
            sb.appendLine("  ( __shelly_quick_check_clis & )")
            sb.appendLine("fi")
            // Original 24h gate kept as fallback (e.g. when quick check is
            // disabled via SHELLY_QUICK_CHECK_INTERVAL=99999999 or every quick
            // check fails network). Quick check fires __shelly_bg_cli_update
            // directly when an upgrade is available, so this branch only
            // matters when the user has been offline for 24h.
            sb.appendLine("if [ \"\${SHELLY_AUTO_UPDATE_CLIS:-0}\" = \"1\" ] && [ \$(( __shelly_now - __shelly_last_update )) -ge \$__shelly_update_interval ]; then")
            sb.appendLine("  ( __shelly_bg_cli_update & )")
            sb.appendLine("fi")
            sb.appendLine()

            // Prompt: PROMPT_COMMAND dynamically builds PS1 with HOME→~ replacement.
            // bash's \w doesn't shorten HOME when launched via linker64, so we do it
            // manually. We previously embedded OSC 133 (shell-integration) markers
            // here, but the bundled TerminalView didn't strip them and they leaked
            // into the visible buffer (\[\e]133;A\a\] showed up literally),
            // throwing off cursor column math and producing the "phantom prompt"
            // bug. Drop OSC 133 entirely — it's only useful to terminals that
            // understand it (Warp, WezTerm) and we don't.
            sb.appendLine("# Prompt with dynamic HOME shortening")
            sb.appendLine("# Resolve real HOME path once (symlink: /data/user/0 vs /data/data)")
            sb.appendLine("SHELLY_HOME_REAL=\$(cd \"\$HOME\" 2>/dev/null && pwd -P)")
            sb.appendLine("[ -z \"\$SHELLY_HOME_REAL\" ] && SHELLY_HOME_REAL=\"\$HOME\"")
            sb.appendLine("__shelly_prompt() {")
            sb.appendLine("  local d")
            sb.appendLine("  d=\"\$(pwd -P 2>/dev/null || pwd)\"")
            sb.appendLine("  printf '%s' \"\$d\" > \"\$HOME/.shelly_cwd\" 2>/dev/null")
            sb.appendLine("  # Replace home path with ~ (\\~ prevents tilde expansion in replacement)")
            sb.appendLine("  d=\"\${d/#\$SHELLY_HOME_REAL/\\~}\"")
            sb.appendLine("  d=\"\${d/#\$HOME/\\~}\"")
            sb.appendLine("  # printf -v assigns to PS1 directly; \\[ \\] stay literal so bash treats")
            sb.appendLine("  # them as PS1 width-hint markers, and \\033 expands to ESC for color.")
            sb.appendLine("  printf -v PS1 '\\[\\033[1;32m\\]%s\\[\\033[0m\\]\\$ ' \"\$d\"")
            sb.appendLine("}")
            sb.appendLine("PROMPT_COMMAND=__shelly_prompt")

            // MOTD — displayed once on first login, then flag file prevents repeat
            sb.appendLine()
            sb.appendLine("# Welcome MOTD (first launch only)")
            sb.appendLine("if [ ! -f \"\$HOME/.shelly_motd_shown\" ]; then")
            sb.appendLine("  printf '\\n'")
            sb.appendLine("  printf '\\033[36m━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\\033[0m\\n'")
            sb.appendLine("  printf '\\033[1;32m  Shelly へようこそ\\033[0m\\n'")
            sb.appendLine("  printf '\\033[36m━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\\033[0m\\n'")
            sb.appendLine("  printf '\\n'")
            sb.appendLine("  printf '  以下のCLIツールがインストール済みです:\\n'")
            sb.appendLine("  printf '\\n'")
            sb.appendLine("  printf '    \\033[33mclaude\\033[0m    — Claude Code (Anthropic)\\n'")
            sb.appendLine("  printf '    \\033[33mgemini\\033[0m    — Gemini CLI  (Google)\\n'")
            sb.appendLine("  printf '    \\033[33mcodex\\033[0m     — Codex CLI   (OpenAI)\\n'")
            sb.appendLine("  printf '\\n'")
            sb.appendLine("  printf '  お持ちのアカウントでログインしてください:\\n'")
            sb.appendLine("  printf '\\n'")
            // v39: corrected login hints.
            //   - `claude auth login` is wrong — Claude Code doesn't have a
            //     standalone auth subcommand. Users launch `claude` and
            //     type `/login` inside the REPL.
            //   - `codex-login` is Shelly's new JS helper (v39) that drives
            //     the ChatGPT subscription device-auth flow and writes
            //     ~/.codex/auth.json. Previously there was no way to
            //     authenticate codex on-device because codex-termux has
            //     the login subcommand compiled out.
            sb.appendLine("  printf '    \\033[90m\$\\033[0m claude           \\033[90m(then type /login)\\033[0m\\n'")
            sb.appendLine("  printf '    \\033[90m\$\\033[0m gemini auth login \\033[90m(Google OAuth)\\033[0m\\n'")
            sb.appendLine("  printf '    \\033[90m\$\\033[0m codex-login      \\033[90m(ChatGPT subscription)\\033[0m\\n'")
            sb.appendLine("  printf '\\n'")
            sb.appendLine("  printf '\\033[36m━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\\033[0m\\n'")
            sb.appendLine("  printf '\\n'")
            sb.appendLine("  touch \"\$HOME/.shelly_motd_shown\"")
            sb.appendLine("fi")

            bashrc.writeText(sb.toString())
            versionFile.writeText(BASHRC_VERSION.toString())
        }

        // Create .profile
        val profile = File(home, ".profile")
        if (!profile.exists()) {
            profile.writeText("[ -f ~/.bashrc ] && . ~/.bashrc\n")
        }

        return home
    }
